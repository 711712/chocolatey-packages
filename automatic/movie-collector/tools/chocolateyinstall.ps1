$checksum     = '74493cd737b25d07165fa44edb4a87f25d7e20b954f5c3a952be31b6272433ce'
$checksumType = 'sha256'

$packageArgs = @{
  packageName    = $env:ChocolateyPackageName
  fileType      = 'exe'
  url            = $url
  checksum       = $checksum
  checksumType   = $checksumType
