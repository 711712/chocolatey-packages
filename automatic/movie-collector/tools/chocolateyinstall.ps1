$checksum     = '41ae8782701ce9f30c376ed1c4bffed612e73727672d2c5b98c62eb1369ee4ac'
$checksumType = 'sha256'

$packageArgs = @{
  packageName    = $env:ChocolateyPackageName
  fileType      = 'exe'
  url            = $url
  checksum       = $checksum
  checksumType   = $checksumType
