$checksum     = '2112516e33553b47d384dd84fb519d22daa28cbb4177f3f557d28020599a23fe'
$checksumType = 'sha256'

$packageArgs = @{
  packageName    = $env:ChocolateyPackageName
  fileType      = 'exe'
  url            = $url
  checksum       = $checksum
  checksumType   = $checksumType
